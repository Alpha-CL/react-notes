import React from "react";
import {Consumer} from "../formContext";


//-------------------------------------------------------------------------------------------------------------------//


function FormButton(props) {

    return (

        <Consumer

        >
            {ctx => {

                return (

                    <button
                        onClick={()=>{

                            ctx.onSubmit();
                        }}

                    >{props.children}</button>
                )
            }}
        </Consumer>
    );
}


//- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -//


export {

    FormButton
};


