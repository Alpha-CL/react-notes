import {useEffect,useState,useCallback,useContext} from "react";


//-------------------------------------------------------------------------------------------------------------------//


function useTimer(func, duration) {

    useEffect(() => {

        const timer = setInterval(func, duration);

        return () => {

            clearInterval(timer);
        };

    },[]);
}


//- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -//


export {

    useTimer
};


