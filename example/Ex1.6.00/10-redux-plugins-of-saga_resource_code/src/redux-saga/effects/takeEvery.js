import {fork} from "./fork";
import {take} from "./take";


//-------------------------------------------------------------------------------------------------------------------//


export function takeEvery(actionType, func, ...args) {

    return fork(function* () {

        while (true) {
            const action = yield take(actionType);
            yield fork(func, ...args.concat(action));
        }
    });
}


