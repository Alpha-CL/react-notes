export {

    withLogin
};


//- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -//


function withLogin(Comp) {

    return function LoginWrapper(props) {

        if (props.isLogin) {

            return (

                <Comp {...props} />
            );

        }

        return null;
    };
}


