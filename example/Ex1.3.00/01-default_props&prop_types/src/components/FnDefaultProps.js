import React from "react";


//-------------------------------------------------------------------------------------------------------------------//


export {
    FnDefaultProps
}


//- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -//


function FnDefaultProps(props) {

    console.log('[FnDefaultProps]: ',props);             // 此时已完成 默认属性 和 传递属性 的混合

    return (

        <div>
            a: {props.a}, b: {props.b}, c: {props.c}
        </div>
    );
}

/** 设置函数静态默认属性值 **/
FnDefaultProps.defaultProps = {

    a: 1,
    b: 2,
    c: 3
};


